import math

def add(a, b):
    """
    Adds two numbers
    """
    res = a + b
    return res

def multiply(a, b):
    """
    Multiplies two numbers
    """
    res = a * b 
    return res

def sqrt(a):
    """
    Calculates square root of a using build-in math library
    """
    result = math.sqrt(a)
    return result


